#include "libft.h"

int			ft_iswhitespacestr(char *s)
{
	int i;

	i = 0;
	while (s[i])
	{
		if ((s[i] < 9 || s[i] > 13) && s[i] != 32)
			return (0);
		i++;
	}
	return (1);
}
