#include "libft.h"

void	ft_strshift(char **st, char c)
{
	int		i;
	char	*tmp;

	i = 0;
	while (st[0][i] != c)
		i++;
	i++;
	tmp = ft_strdup(&(st[0][i]));
	free(*st);
	*st = tmp;
}
