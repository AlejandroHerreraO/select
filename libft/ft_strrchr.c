#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char *r;

	r = NULL;
	while (*s != '\0')
	{
		if (*s == c)
			r = (char *)s;
		s++;
	}
	if (*s == c)
		r = (char *)s;
	return (r);
}
