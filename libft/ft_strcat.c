#include "libft.h"

char	*ft_strcat(char *dst, const char *src)
{
	char *t;

	t = dst;
	while (*dst != '\0')
		dst++;
	while (*src != '\0')
	{
		*dst = *src;
		dst++;
		src++;
	}
	*dst = '\0';
	return (t);
}
