char	*ft_strrev(char *str)
{
	int		i;
	int		j;
	char	a;

	i = 0;
	j = 0;
	while (str[i] != '\0')
		i++;
	i--;
	while (j < i)
	{
		a = str[i];
		str[i] = str[j];
		str[j] = a;
		j++;
		i--;
	}
	return (str);
}
