#include "libpf.h"

void	cleanse_str(char **s)
{
	char *tmp;

	if (((s[0][0] < '0' || s[0][0] > '9') && (s[0][0] < 'a' || s[0][0] > 'f')
		&& (s[0][0] < 'A' || s[0][0] > 'F'))
		|| (s[0][0] == '0' && s[0][1] != 0))
	{
		tmp = ft_strdup(&(s[0][1]));
		free(*s);
		*s = tmp;
	}
}

int		is_hex(char *s)
{
	if (s[0] == '0' && s[1] == 'x')
		return (1);
	return (0);
}

int		is_oct(char *s)
{
	if (s[0] == '0' && s[1] == 'X')
		return (1);
	return (0);
}
